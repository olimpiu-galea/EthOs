export function overdueActions(items: MaintenanceAction[]) {
  return items.filter((i) => i.status === "Overdue");
}

export function dueThisWeekActions(items: MaintenanceAction[]) {
  return items.filter((i) => {
    if (i.status === "Compliant") return false;
    const days = daysUntilDue(i.dueDate);
    return days >= 0 && days <= 7;
  });
}

export function criticalAssetRiskActions(items: MaintenanceAction[]) {
  return items.filter(
    (i) => i.riskLevel === "critical" || i.criticality === "critical",
  );
}

export function criticalOverdueActions(items: MaintenanceAction[]) {
  return items.filter(
    (i) => i.status === "Overdue" && i.criticality === "critical",
  );
}

export function partsBlockingActions(items: MaintenanceAction[]) {
  return items.filter(
    (i) => i.status !== "Compliant" && !i.requiredPartsAvailable,
  );
}

export function maintenanceActionKpis(items: MaintenanceAction[]) {
  return {
    total: items.length,
    overdue: overdueActions(items).length,
    dueThisWeek: dueThisWeekActions(items).length,
    criticalAssetRisks: criticalAssetRiskActions(items).length,
  };
}
