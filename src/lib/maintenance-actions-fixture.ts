/** Demo "today" anchor so Due-this-week logic is deterministic against the fixture dates */
export const MAINTENANCE_REFERENCE_TODAY = "2026-06-15";

export function daysUntilDue(
  dueDate: string,
  today: string = MAINTENANCE_REFERENCE_TODAY,
): number {
  const due = new Date(`${dueDate}T00:00:00`).getTime();
  const ref = new Date(`${today}T00:00:00`).getTime();
  return Math.round((due - ref) / (24 * 60 * 60 * 1000));
}

